    static func find(_ id: String) -> ExerciseDefinition? {
        exercises.first { $0.id == id }
    }

    static func startingWeight(for exercise: ExerciseDefinition, userWeight: Int) -> Double {
        guard !exercise.isBodyweight else { return 0 }
        return round(Double(userWeight) * exercise.bodyweightMultiplier / 5) * 5
    }

    /// Personalized starting weight that accounts for user profile: experience level
    /// multiplier and optional benchmark 1RMs for more accurate initial loads.
    static func startingWeight(for exercise: ExerciseDefinition, profile: UserProfile) -> Double {
        guard !exercise.isBodyweight else { return 0 }

        // First, check if the user provided a relevant benchmark 1RM
        if let benchmarkWeight = profile.workingWeightFromBenchmark(
            exerciseId: exercise.id,
            increment: exercise.weightIncrement
        ) {
            // Apply experience multiplier to benchmark-derived weight too
            let adjusted = benchmarkWeight * profile.experienceMultiplier
            return ProgressionEngine.roundToNearest(adjusted, increment: exercise.weightIncrement)
        }

        // Fall back to bodyweight multiplier with experience adjustment
        let baseWeight = Double(profile.weight) * exercise.bodyweightMultiplier
        let adjusted = baseWeight * profile.experienceMultiplier
        return ProgressionEngine.roundToNearest(adjusted, increment: exercise.weightIncrement)
    }
